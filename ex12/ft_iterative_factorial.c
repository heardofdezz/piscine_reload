int		ft_iterative_factorial(int nb)
{
	int		res;

	if (nb < 0 || nb > 12)
		return (0);
	else
	{
		res = 1;
		while (nb > 0)
		{
			res *= nb;
			nb--;
		}
		return (res);
	}
}
